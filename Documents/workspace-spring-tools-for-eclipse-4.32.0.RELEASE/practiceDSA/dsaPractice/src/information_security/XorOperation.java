package information_security;

public class XorOperation {
	
	  public static void main(String[] args) {
		  
		  int a=5;
		  int b=7;
		  
		  System.out.println(a^b);
		  
		  
		  int c=10;
		  int d=7;
		  
		  System.out.println(c^d);
		  
		  char x='A';
		  char y='B';
		  char xor=(char) (x^y);
		  System.out.println(xor);
		  System.out.println((int)xor);
		  
		  
	  }

}
